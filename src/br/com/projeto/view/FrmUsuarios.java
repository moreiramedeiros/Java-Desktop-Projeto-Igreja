    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {

            Usuarios obj = new Usuarios();
            Utilitarios util = new Utilitarios();

            obj.setNome(txtNome.getText());
            obj.setData_nascimento(util.converteDataBrParaUSA(txtData_nascimento.getText()));
            obj.setRg(txtRg.getText());
            obj.setCpf(txtCpf.getText());
            obj.setEmail(txtEmail.getText());
            obj.setTelefone(txtTelefone.getText());
            obj.setCelular(txtCelular.getText());
            obj.setCep(txtCep.getText());
            obj.setEndereco(txtEndereco.getText());
            obj.setNumero(Integer.parseInt(txtNumero.getText()));
            obj.setComplemento(txtComplemento.getText());
            obj.setBairro(txtBairro.getText());
            obj.setCidade(txtCidade.getText());
            obj.setEstado(cbEstado.getSelectedItem().toString());

            if (adm_local) {
                obj.setIgreja((Igrejas) cbIgreja.getSelectedItem());
            } else {
                Igrejas igreja = (Igrejas) cbIgreja.getSelectedItem();
                igreja.setId(-1);
                obj.setIgreja(igreja);
            }

            obj.setSenha(txtSenha.getText());
            obj.setLogin(txtLogin.getText());
            obj.setNivel_acesso(cbNivel_acesso.getSelectedItem().toString());

            UsuariosDAO dao = new UsuariosDAO();
            dao.cadastrarUsuario(obj);
            limparCampos();
            id = 0;
        } catch (java.time.format.DateTimeParseException e1) {
            JOptionPane.showMessageDialog(null, "Dados inválidos no campo 'Data de Nascimento'");
        } catch (java.lang.NumberFormatException e2) {
            JOptionPane.showMessageDialog(null, "Dados inválidos no campo 'Número'.");
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

