    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Botão Pesquisar
        Utilitarios util = new Utilitarios();
        String nome = "%" + txtPesquisa.getText().trim() + "%";
        UsuariosDAO dao = new UsuariosDAO();
        List<Usuarios> lista = dao.buscarUsuarioPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaUsuarios.getModel();
        dados.setNumRows(0);

        for (Usuarios c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getNivel_acesso(),
                c.getLogin(),
                c.getSenha(),
                util.converteDataUsaParaBr(c.getData_nascimento()),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getIgreja().getNome()
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed

