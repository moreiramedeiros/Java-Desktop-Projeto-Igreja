    public void preencheFormulario() {
        Usuarios u = this.usuario;

        
        Utilitarios util = new Utilitarios();
        txtNome.setText(u.getNome());

        cbNivel_acesso.setSelectedItem(u.getNivel_acesso());

        txtLogin.setText(u.getLogin());
        txtSenha.setText(u.getSenha());

        txtData_nascimento.setText(util.converteDataUsaParaBr(u.getData_nascimento()));
        txtRg.setText(u.getRg());
        txtCpf.setText(u.getCpf());
        txtEmail.setText(u.getEmail());
        txtTelefone.setText(u.getTelefone());
        txtCelular.setText(u.getCelular());
        txtCep.setText(u.getCep());
        txtEndereco.setText(u.getEndereco());
        txtNumero.setText(u.getNumero() + "");
        txtComplemento.setText(u.getComplemento());
        txtBairro.setText(u.getBairro());
        txtCidade.setText(u.getCidade());
        cbEstado.setSelectedItem(u.getEstado());

        try {
            String nomeIgrejaTabela = u.getIgreja().getNome();
            for (int i = 0; i < cbIgreja.getItemCount(); i++) {
                Igrejas igreja = (Igrejas) cbIgreja.getItemAt(i);
                if (nomeIgrejaTabela.equals(igreja.getNome())) {
                    cbIgreja.setSelectedItem(igreja);
                }
            }
        } catch (java.lang.NullPointerException e) {

        }
    }

