    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Botão Pesquisar
        Utilitarios util = new Utilitarios();
        String nome = "%" + txtPesquisa.getText().trim() + "%";
        IgrejasDAO dao = new IgrejasDAO();
        List<Igrejas> lista = dao.buscaIgrejaPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaIgrejas.getModel();
        dados.setNumRows(0);

        for (Igrejas c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                util.converteDataUsaParaBr(c.getData_fundacao()),
                c.getCnpj(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado()
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed

