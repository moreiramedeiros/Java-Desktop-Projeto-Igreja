    public void listar() {
        Utilitarios util = new Utilitarios();
        IgrejasDAO dao = new IgrejasDAO();
        List<Igrejas> lista = dao.listarIgrejas();
        DefaultTableModel tabela = (DefaultTableModel) tabelaIgrejas.getModel();
        tabela.setNumRows(0);

        for (Igrejas c : lista) {
            tabela.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                util.converteDataUsaParaBr(c.getData_fundacao()),
                c.getCnpj(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado()
            });
        }
    }

