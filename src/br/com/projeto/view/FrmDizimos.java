    //Listar igrejas
    public void listar() {
        Utilitarios util = new Utilitarios();
        DizimosDAO dao = new DizimosDAO();
        List<Dizimos> lista = dao.listarDizimos();
        DefaultTableModel tabela = (DefaultTableModel) tabelaMembros.getModel();
        tabela.setNumRows(0);

        for (Dizimos c : lista) {
            tabela.addRow(new Object[]{
                c.getId(),
                c.getMembro().getNome(),
                c.getValor(),
                util.converteDataUsaParaBr(c.getData_dizimo()),
                c.getIgreja().getNome()
            });
        }
    }

