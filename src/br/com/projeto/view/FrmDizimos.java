    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {

            Dizimos obj = new Dizimos();
            Utilitarios util = new Utilitarios();
            String valor_ = txtValor.getText();
            valor_ = valor_.replace(",",".");
            obj.setValor(Double.parseDouble(valor_));
            obj.setData_dizimo(util.converteDataBrParaUSA(txtData_dizimo.getText()));
            obj.setMembro((Membros) cbMembros.getSelectedItem());
            obj.setIgreja((Igrejas) cbIgrejas.getSelectedItem());
            DizimosDAO dao = new DizimosDAO();
            dao.cadastrarDizimo(obj);
            limparCampos();
            id = 0;
        } catch (java.time.format.DateTimeParseException e1) {
            JOptionPane.showMessageDialog(null, "Dados inválidos no campo 'Data de Nascimento'");
        } catch (java.lang.NumberFormatException e2) {
            JOptionPane.showMessageDialog(null, "Dados inválidos no campo 'Valor'.");
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

