    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Botão Pesquisar
        Utilitarios util = new Utilitarios();
        String nome = "%" + txtPesquisa.getText().trim() + "%";
        DizimosDAO dao = new DizimosDAO();
        List<Dizimos> lista = dao.buscaDizimistasPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaMembros.getModel();
        dados.setNumRows(0);

        for (Dizimos c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getMembro().getNome(),
                c.getValor(),
                util.converteDataUsaParaBr(c.getData_dizimo()),
                c.getIgreja().getNome()
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed

