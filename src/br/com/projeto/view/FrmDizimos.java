    public void listarIgrejaComboBox() {

        IgrejasDAO dao = new IgrejasDAO();
        List<Igrejas> lista = dao.listarIgrejas();
        cbIgrejas.removeAllItems();

        for (Igrejas c : lista) {
            cbIgrejas.addItem(c);
        }
    }

    public void listarMembrosComboBox(Igrejas obj) {

        MembrosDAO dao = new MembrosDAO();
        List<Membros> lista = dao.listarMembrosPorIgreja(obj);
        cbMembros.removeAllItems();

        for (Membros c : lista) {
            cbMembros.addItem(c);
        }
       
    }

