    private void tabelaMembrosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMembrosMousePressed
        // TODO add your handling code here:
        // TODO add your handling code here:

        jTabbedPane1.setSelectedIndex(0);
        id = Integer.parseInt(tabelaMembros.getValueAt(tabelaMembros.getSelectedRow(), 0).toString());

        txtValor.setText(tabelaMembros.getValueAt(tabelaMembros.getSelectedRow(), 2).toString());
        txtData_dizimo.setText(tabelaMembros.getValueAt(tabelaMembros.getSelectedRow(), 3).toString());

        String nomeIgreja = tabelaMembros.getValueAt(tabelaMembros.getSelectedRow(), 4).toString();

        for (int i = 0; i < cbIgrejas.getItemCount(); i++) {
            Igrejas igreja = (Igrejas) cbIgrejas.getItemAt(i);
            if (nomeIgreja.equals(igreja.getNome())) {
                cbIgrejas.setSelectedItem(igreja);
            }
        }

        String nomeMembro = tabelaMembros.getValueAt(tabelaMembros.getSelectedRow(), 1).toString();

        for (int i = 0; i < cbMembros.getItemCount(); i++) {
            Membros membros = (Membros) cbMembros.getItemAt(i);
            if (nomeMembro.equals(membros.getNome())) {
                cbMembros.setSelectedItem(membros);
            }
        }


    }//GEN-LAST:event_tabelaMembrosMousePressed

